    void randevuSirasi(){
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("Omer_Kocbil_1321221009_BP2_ODEV2PU");
        EntityManager em = emf.createEntityManager();
        Query q = em.createQuery("SELECT h FROM Randevu h");
        List <Randevu> hastalar = q.getResultList();
        EntityManagerFactory emf2 = Persistence.createEntityManagerFactory("Omer_Kocbil_1321221009_BP2_ODEV2PU");
        EntityManager em2 = emf2.createEntityManager();
        for (Randevu hasta : hastalar) {
            Query q2 = em2.createQuery("SELECT h FROM Hasta h WHERE h.hastaPK.kullaniciAdi=:hAdi");
            q2.setParameter("hAdi", hasta.getKullanici());
            Hasta h=(Hasta)q2.getSingleResult();
            Object[] o={h.getAd(),h.getSoyad(),hasta.getRandevuKodu()};
            dtm.addRow(o);
        }
    }
    
